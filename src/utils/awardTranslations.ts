export function translateAward(award: string, language: string): string {
    const normalizedAward = award.trim();
    const translation = awardTranslations[normalizedAward];
    
    if (!translation) {
        return award;
    }
    
    const langMap: { [key: string]: string } = {
        'zh': 'zh',
        'zh-CN': 'zh',
        'zh-TW': 'zh-TW',
        'zh-HK': 'zh-HK',
        'en-US': 'en',
        'en-GB': 'en',
        'en': 'en',
        'ja': 'ja',
        'ko': 'ko',
        'es': 'es',
        'es-ES': 'es',
        'es-MX': 'es',
        'fr': 'fr',
        'de': 'de',
        'it': 'it',
        'pt': 'pt',
        'pt-PT': 'pt',
        'pt-BR': 'pt',
        'ru': 'ru',
        'tr': 'tr'
    };
    
    const mappedLang = langMap[language] || 'en';
    return translation[mappedLang] || translation['en'] || award;
}
